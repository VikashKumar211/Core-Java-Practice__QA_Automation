package ex_11_While;

public class Lab115_While {
    public static void main(String[] args) {
        int i = 10;  //initialization
        while(i<=10){ //condition
            System.out.println(i);
            i++;  //updation
        }

        System.out.println("------");

        // initialization  // condition // updation
        for (int j = 0; j < 5; j++) {
            System.out.println(j);

        }
    }
}
