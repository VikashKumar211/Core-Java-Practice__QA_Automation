package ex_07_Increment_Decrement_Op;

public class Lab072_ID {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int result1 = ++a;
        int result2 = b++;
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(b);
    }
}
